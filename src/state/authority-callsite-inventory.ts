function entry(
  path: string,
  symbol: string,
  classification: AuthorityCallsiteClassification,
  owner: string,
  rationale: string,
  migration_phase: string,
): AuthorityCallsiteInventoryEntry {
  return { path, symbol, classification, owner, rationale, migration_phase };
}

