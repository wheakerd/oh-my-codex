/**
 * Canonical authority-surface inventory for issue #3139.
 *
 * This is a production inventory, not a list of approved compatibility APIs.
 * A row exists for every currently known root resolver, run-root registry path,
 * or caller that can carry one of those values to an execution boundary. The
 * compiler-API test keeps the rows synchronized with the source tree.
 */

export type AuthorityCallsiteClassification =
  | 'authority-context'
  | 'bootstrap-only'
  | 'intentionally-cwd-local'
  | 'out-of-scope';

export interface AuthorityCallsiteInventoryEntry {
  path: string;
  symbol: string;
  classification: AuthorityCallsiteClassification;
  owner: string;
  rationale: string;
  migration_phase: string;
}

export interface AuthorityDynamicCallWaiver {
  path: string;
  symbol: string;
  source_range: `${number}:${number}-${number}:${number}`;
  classification: AuthorityCallsiteClassification;
  owner: string;
  rationale: string;
  bounded_targets: readonly string[];
  migration_phase: string;
  expiry_or_removal_condition: string;
}

/**
 * A dynamic process-environment key is reviewable only at one exact source
 * range. Waivers require an inventory classification and a concrete removal
 * condition; they cannot cover a file, symbol, or wildcard range.
 */
export interface AuthorityDynamicEnvAccessWaiver {
  path: string;
  symbol: string;
  source_range: `${number}:${number}-${number}:${number}`;
  target_text: string;
  classification: AuthorityCallsiteClassification;
  owner: string;
  rationale: string;
  migration_phase: string;
  expiry_or_removal_condition: string;
}

